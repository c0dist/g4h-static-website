Firstly, the venue. Goa ! Oh, it was divine. Frankly, nullcon should be always in Goa. Perfect blend of electric atmosphere and aesthetic calmness of nature. Seriously, you have to be there to feel it.<br />
<br />
Me, Ap4Ch3, akm.coder and Snypter arrived 2 days ahead of conference. Had a great time exploring Goa. Already, most of the people from null were beginning to pour in. <br />
<br />
------------------------------------------------------------<br />
<br />
The evening before the 1st day of conference, we were relaxing in one of the open air restaurants near beach and getting to know other people. News from the nearby Jailbreak event was coming in. Almost all sympathised with participants when they came to know about the push ups,15 min break only and other tough conditions :p<br />
<br />
D4rk357,fb1h2s,Neo, the_empty,Godwin Austin, 41.w4r10r, Utsav and Umesh sir arrived too and reserved a table.We ordered dinner sipping beer meanwhile.  Don't exactly remember all the topics, but I have vague memories of discussions looking into the mechanisms of how Internet is provided in commercial flights (Wifi) in air via satellite. Hacker minds immediately noticed the security threat that maybe the aeroplane can be hacked into and controlled remotely if it was the same satellite uplink used by air control towers. Or even by a passenger inside the aeroplane. Some other talks about USRP and GSM.(I think I proposed something about starting a irc class of sorts for Garage members... but I am not sure I did it or not :confused:)<br />
<br />
The atmosphere was electric and it felt like pool of awesomeness all around. Anant arrived for some minutes and all said our hellos. (He was already working hard for con preparations the next day ... and boy he can multi-task superbly :cool:)<br />
